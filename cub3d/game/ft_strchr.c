#include "header.h"

int			ft_strchr(const char *s, int c)
{
	char	*p;

	p = (char *)s;
	while (*p != '\0')
	{
		if (*p == c)
			return (1);
		p++;
	}
	if (*p == c)
		return (1);
	return (-1);
}
